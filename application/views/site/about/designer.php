<h2>DESENHE MÓVEIS PARA A GALATEA</h2>
<br>

<p>Aqui sua ideia vira produto</p>
<br>

<p>Sabemos o quanto é difícil para um designer de móveis tirar seu projeto do papel. Muitas vezes ótimas ideias esbarram em pré-requisitos do mercado tradicional: materiais baratos, produção em grande escala, enorme margem de lucro das empresas e baixíssimo retorno ao designer. A GALATEA quer mudar essa história. Apostamos no conceito de <?php echo  anchor('http://pt.wikipedia.org/wiki/Crowdsourcing', 'crowdsourcing') ?> para eliminar essas barreiras e fazer a ligação direta entre quem cria e quem consome móveis de alta qualidade.
</p>
<br>

<p>Envie seu projeto para a gente. Se ele for aprovado, a GALATEA cuidará de todo o processo: prototipagem, produção, comercialização e distribuição do seu produto. Você receberá royalties de acordo com as vendas.
</p>
<br>

<p>O seu nome sempre estará ligado ao produto que você criou, com divulgação apropriada. Você terá na GALATEA uma ótima porta de entrada para o mercado de móveis e design de produtos, além de uma fonte de renda.
</p>
<br>

<p>Veja como funciona.</p>
<br>

<h3>Mostre o seu talento!</h3>

<p>
  Envie o seu desenho especificando os detalhes técnicos na inscrição. Sua ideia seguirá para a Curadoria GALATEA, que analisará a viabilidade de produção e de potencial de mercado. Em cinco dias entraremos em contato para informar se o projeto seguirá para a próxima etapa: a votação.
</p>
<br>

<h3>Entre na votação</h3>
<p>
  Se o projeto for escolhido pela Curadoria, ele segue para votação no site. É a própria comunidade GALATEA que decidirá quais projetos virarão realidade. Funciona assim: no primeiro dia de cada mês, os projetos escolhidos viram candidatos à produção. A votação dura um mês, e, ao final deste período, o mais votado pelos visitantes do site passa para a fase de produção.
</p>
<br>

<p>
  Se algum projeto seu estiver concorrendo, você poderá acompanhar o número de votos no site. Aproveite para divulgar a sua ideia nas redes sociais e aumente as suas chances na votação.
</p>
<br>

<h3>A ideia sai do papel</h3>

<p>
  Entraremos em contato com o designer vencedor para transformar o projeto em produto. Um protótipo será produzido, definiremos processos de logística e estipularemos um preço para a peça. Isso estará especificado em um contrato com todas as garantias para o designer.
</p>
<br>

<p>
  Ou seja, o produto que você desenhou levará o seu crédito e você receberá uma participação sobre as vendas realizadas. A parceria valerá por 2 anos, com exclusividade de produção e comercialização da GALATEA.
</p>
<br>

<h3>Ganhe com sua ideia</h3>

<p>
  Achamos que a forma mais justa de remunerar os designers é por meio de royalties. Produtos que venderem mais, renderão mais dinheiro aos criadores. Simples assim.
</p>
<br>

<p>
  Na GALATEA, o designer recebe 5% do valor de cada produto seu vendido no site. Essa quantia poderá ser depositada em conta ou virar descontos em compras futuras na GALATEA. Designers podem receber via transferência bancária como Pessoa Física, e não apenas como Pessoa Jurídica. Neste caso, o pagamento será feito mediante envio de RPA (recibo de pagamento a autônomo).
</p>
