<p><strong>Cookies </strong></p>
<br>

<p>O usuário da Galatea manifesta conhecer e aceitar que poderá ser utilizado um sistema de monitoramento mediante a utilização de cookies. Os cookies são pequenos arquivos que se instalam no disco rígido, com uma duração limitada de tempo que ajudam a personalizar os serviços. Também serão ofertadas algumas funcionalidades que só estarão disponíveis pelo emprego dos cookies. Os cookies são utilizados com o fim de conhecer os interesses, o comportamento e a demografia de quem visita ou é usuário da página de internet, e dessa forma, compreender melhor suas necessidades e interesses e dar-lhes um serviço melhor ou prover-lhes informação relacionada. Também será usada a informação obtida por intermédio dos cookies para analisar as páginas navegadas pelo visitante ou usuário, as buscas realizadas, melhorar as iniciativas comerciais e promocionais, mostrar publicidade ou promoções, banners de interesse, notícias sobre a Galatea, aperfeiçoar a oferta de conteúdos e bens, personalizar esses conteúdos, apresentação e serviços; também poderá haver utilização de cookies para promover e fazer cumprir as regras e segurança da página de internet. A Galatea poderá adicionar cookies nos e-mails que envia para medir a efetividade das promoções.</p>
<br>

<p>Utiliza-se adicionalmente os cookies para que o usuário não tenha que introduzir sua senha tão freqüentemente durante uma seção de navegação, também para contabilizar e corroborar os registos (nos países nos quais está disponível), e outros acordos comerciais, sendo o objetivo da instalação dos cookies, o benefício do usuário que o recebe, e não será usado com outros fins alheios à operação da Galatea.</p>
