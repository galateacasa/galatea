<p><strong>Web Beacons </strong></p>
<br>

<p>Um Web beacon é uma imagem eletrônica, também chamada semgle-pixel (1 x 1) ou pixel transparente, que é colocada em código em uma página de internet. Um Web beacon tem finalidades similares aos cookies. Adicionalmente, um Web beacon é utilizado para medir padrões de tráfego dos utilizadores de uma página a outra com objetivo de maximizar o fluxo de tráfego através da web. O usuário e o visitante da página de internet da Galatea manifestam conhecer e aceitam que Galatea poderá utilizar um sistema de monitoramento mediante a utilização de Web beacons.</p>
