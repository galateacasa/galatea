<h3>O processo funciona assim:</h3>

<p>1. Assim que um projeto vence a votação, a GALATEA divulga o produto para sua base de fornecedores via e-mail. Todas as especificações (dimensões, materiais, previsão de demanda) estarão disponíveis no site;
</p>
<p>2. O fornecedor que se interessar em produzir o produto faz sua proposta para a GALATEA;
</p>
<p>3. Aquela que atender melhor nossos requisitos (localização, nível de serviço, qualidade, custo e prazo de produção) será escolhido para produzir um protótipo e assinará contrato comercial;
</p>
<p>4. Aprovado o protótipo, o produto ficará disponível para venda no site. A cada venda realizada, o fornecedor receberá um email com o pedido de produção.
</p>
